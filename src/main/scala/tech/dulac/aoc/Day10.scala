  @tailrec
  def processLine(line: List[String], closings: List[String]): (Boolean, List[String]) = (line, closings) match {
    case (Nil, _)                                   => (false, closings)
    case (x :: xs, _) if x == "["                   => processLine(xs, "]" :: closings)
    case (x :: xs, _) if x == "("                   => processLine(xs, ")" :: closings)
    case (x :: xs, _) if x == "{"                   => processLine(xs, "}" :: closings)
    case (x :: xs, _) if x == "<"                   => processLine(xs, ">" :: closings)
    case (x :: xs, c :: cs) if x == "]" && c == "]" => processLine(xs, cs)
    case (x :: xs, c :: cs) if x == ")" && c == ")" => processLine(xs, cs)
    case (x :: xs, c :: cs) if x == "}" && c == "}" => processLine(xs, cs)
    case (x :: xs, c :: cs) if x == ">" && c == ">" => processLine(xs, cs)
    case (x :: _, _) if x == ")"                    => (true, List(")"))
    case (x :: _, _) if x == "]"                    => (true, List("]"))
    case (x :: _, _) if x == "}"                    => (true, List("}"))
    case (x :: _, _) if x == ">"                    => (true, List(">"))
  }

